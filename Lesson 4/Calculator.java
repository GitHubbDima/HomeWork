package com.step.calculator;

public class Calculator{
	
	public double gathering(double num1, double num2){
		return num1 + num2;
	}
	
	public double decreased(double num1, double num2){
		return num1 - num2;
	}
	
	public double multiplying(double num1, double num2){
		return num1 * num2;
	}
	
	public double division(double num1, double num2){
		return num1 / num2;
	}
}