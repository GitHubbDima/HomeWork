package com.step.Calculator;

public class Calculator {

    public int gathering(int x, int y){
        return x + y;
    }
    public double gathering(double x, double y){
        return x + y;
    }

    public int decreased(int x, int y){
        return x - y;
    }
    public double decreased(double x, double y){
        return x - y;
    }

    public int multiplying(int x, int y){
        return x * y;
    }
    public double multiplying(double x, double y){
        return x * y;
    }

    public int division(int x, int y){
        return x / y;
    }
    public double division(double x, double y){
        return x / y;
    }

}
